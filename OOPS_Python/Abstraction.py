#🎯 What is Abstraction?
#✅ Definition:
#Abstraction is one of the 4 main principles of OOP (Object-Oriented Programming).
#It means showing only the essential features of an object while hiding the unnecessary details.

#In simple words:
#Abstraction = Hiding complexity + Showing only relevant information



#📌 Key Points
#1️⃣ Abstract Class:

#Cannot be instantiated (can’t create an object of it).

#Meant to be inherited by other classes.

#2️⃣ Abstract Method:

#A method declared, but without implementation in the base class.

#Subclasses must implement it.